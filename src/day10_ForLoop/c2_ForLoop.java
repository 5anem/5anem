package day10_ForLoop;

public class c2_ForLoop {
    public static void main(String[] args) {

        //loops will help you on continues action


        //in yhe loop
        //You willl always need to specify(belirtmek)
        //starting point
        //ending poing
        //iteration

        for (int i=0 ; i<=10 ; i++){ //i =0 ,1,2,3,4...10 == 11 times
            System.out.println("Hello world");
        }//i=1,2


        //start point is 0
        //end point is 7
        //iteration is +2
        for (int i=0 ; i<8 ; i+=2) {
            System.out.println("java");//0 2 4 6
        }//0 2 4 6--4 times

        //start point is 1
        // end point is 10
        //iteration is +3
        for (int k=1 ; k<=10; k+=3){
            System.out.println("Sanem");  //1 4 7 10----4 times
        }










































































    }
}
